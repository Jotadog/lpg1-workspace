package br.edu.ifc.videira.LISTA08;

public class Ex03Animal {
	private String nome;
	private String raca;
	
	public Ex03Animal(String nome, String raca) {
		this.nome = nome;
		this.raca = raca;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getRaca() {
		return raca;
	}

	public void setRaca(String raca) {
		this.raca = raca;
	}
	
}
